package 算法.冒泡排序;

/**
 * 交换算法
 * @author Mindy
 *
 */
public class Demo01 {
	
	 public static void main(String[] args) {
		
		 
		 int num01 = 100;
		 int num02 = 90;

		 System.out.println("交换前:num01 : num02 "+num01+" : "+num02);
		 
		 int temp = num01;
		 num01 = num02;
		 num02 = temp;
		 
		 System.out.println("交换前:num01 : num02 "+num01+" : "+num02);
	}
	

}
